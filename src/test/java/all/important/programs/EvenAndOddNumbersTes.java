package all.important.programs;

import java.util.Arrays;
import java.util.Scanner;

public class EvenAndOddNumbersTes {
	
	public static void main(String[] args) {


		for(int i=0; i<=100; i++) {
			//even
			if(i%2==0) {
				System.out.println(i);
			}
			
			//odd
			if(i%2==1) {
				System.out.println(i);
			}
		}

	}
}

